package com.deb8.model.stat;

public class PostStatistic {
	private TopicStatistic topicStat;
	private UserStatistic userStat;

	public TopicStatistic getTopicStat() {
		return topicStat;
	}

	public void setTopicStat(TopicStatistic topicStat) {
		this.topicStat = topicStat;
	}

	public UserStatistic getUserStat() {
		return userStat;
	}

	public void setUserStat(UserStatistic userStat) {
		this.userStat = userStat;
	}
}
